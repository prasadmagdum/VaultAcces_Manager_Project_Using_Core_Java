public class User {
    public int regId;
    public String name;
    public int age;
    public String email;
    public String username;
    public String password;

    public User(int regId, String name, int age, String email, String username, String password) {
        this.regId = regId;
        this.name = name;
        this.age = age;
        this.email = email;
        this.username = username;
        this.password = password;
    }
}
